namespace Nutela.Utilities
{
    public class Vector3Nutela
    {
        private float _x;
        private float _y;
        private float _z;

        public Vector3Nutela(
            float x,
            float y,
            float z)
        {
            this._x = x;
            this._y = y;
            this._z = z;
        }
    }
}